package com.andersen_trainee.travel_agency_servlet.entity;

public class Hotel {
	private int id;
	private String stars;
	private int coast;

	public Hotel() {

	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getStars() {
		return stars;
	}

	public void setStars(String stars) {
		this.stars = stars;
	}

	public int getCoast() {
		return coast;
	}

	public void setCoast(int coast) {
		this.coast = coast;
	}
	
	
}
