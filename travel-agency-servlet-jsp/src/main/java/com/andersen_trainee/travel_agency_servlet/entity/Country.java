package com.andersen_trainee.travel_agency_servlet.entity;

public class Country {
	private int id;
	private String name;
	private int coast;

	public Country() {

	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getCoast() {
		return coast;
	}

	public void setCoast(int coast) {
		this.coast = coast;
	}
	
	
}
